package com.sp.app.mypage;

public class Mypage {
	private long memberNo;
	private String email;
	private String pwd;
	private String name;
	private String nickName;
	private String birth;
	private String gender;
	private String joindate;
	private String recentdate;
	private String wrongcnt;
	private String modifydate;
	private int enabled;
	private int membership;
	
	public final long getMemberNo() {
		return memberNo;
	}

	public final void setMemberNo(long memberNo) {
		this.memberNo = memberNo;
	}

	public final String getEmail() {
		return email;
	}

	public final void setEmail(String email) {
		this.email = email;
	}

	public final String getPwd() {
		return pwd;
	}

	public final void setPwd(String pwd) {
		this.pwd = pwd;
	}

	public final String getName() {
		return name;
	}

	public final void setName(String name) {
		this.name = name;
	}

	public final String getNickName() {
		return nickName;
	}

	public final void setNickName(String nickName) {
		this.nickName = nickName;
	}

	public final String getBirth() {
		return birth;
	}

	public final void setBirth(String birth) {
		this.birth = birth;
	}

	public final String getGender() {
		return gender;
	}

	public final void setGender(String gender) {
		this.gender = gender;
	}

	public final String getJoindate() {
		return joindate;
	}

	public final void setJoindate(String joindate) {
		this.joindate = joindate;
	}

	public final String getRecentdate() {
		return recentdate;
	}

	public final void setRecentdate(String recentdate) {
		this.recentdate = recentdate;
	}

	public final String getWrongcnt() {
		return wrongcnt;
	}

	public final void setWrongcnt(String wrongcnt) {
		this.wrongcnt = wrongcnt;
	}

	public final String getModifydate() {
		return modifydate;
	}

	public final void setModifydate(String modifydate) {
		this.modifydate = modifydate;
	}

	public final int getEnabled() {
		return enabled;
	}

	public final void setEnabled(int enabled) {
		this.enabled = enabled;
	}

	public final int getMembership() {
		return membership;
	}

	public final void setMembership(int membership) {
		this.membership = membership;
	}
	
}
