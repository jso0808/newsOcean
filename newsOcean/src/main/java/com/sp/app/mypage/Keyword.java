package com.sp.app.mypage;

public class Keyword {
	private int keywordNo;
	private String keywordName;
	private int memberNo;
	
	public final int getMemberNo() {
		return memberNo;
	}
	public final void setMemberNo(int memberNo) {
		this.memberNo = memberNo;
	}
	public final int getKeywordNo() {
		return keywordNo;
	}
	public final void setKeywordNo(int keywordNo) {
		this.keywordNo = keywordNo;
	}
	public final String getKeywordName() {
		return keywordName;
	}
	public final void setKeywordName(String keywordName) {
		this.keywordName = keywordName;
	}
	
}
