package com.sp.app.main;

public class Picknews {
	private long pickNo;
	private long memberNo;
	private String pickTitle;
	private String pickDate;
	private String pickLink;
	private String keyword;
	
	
	public long getPickNo() {
		return pickNo;
	}
	public void setPickNo(long pickNo) {
		this.pickNo = pickNo;
	}
	public long getMemberNo() {
		return memberNo;
	}
	public void setMemberNo(long memberNo) {
		this.memberNo = memberNo;
	}
	public String getPickTitle() {
		return pickTitle;
	}
	public void setPickTitle(String pickTitle) {
		this.pickTitle = pickTitle;
	}
	public String getPickDate() {
		return pickDate;
	}
	public void setPickDate(String pickDate) {
		this.pickDate = pickDate;
	}
	public String getPickLink() {
		return pickLink;
	}
	public void setPickLink(String pickLink) {
		this.pickLink = pickLink;
	}
	public String getKeyword() {
		return keyword;
	}
	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}
	
}
